	<div class='explicacion_articulo'>

		<p>En cumplimiento del Reglamento (UE) 2016/679 del Parlamento Europeo
			y del Consejo, de 27 de abril de 2016, relativo a la protección de
			las personas físicas en lo que respecta al tratamiento de datos
			personales, por el que se deroga la directiva 95/46/CE (en adelante,
			RGPD), de la Ley 34/2002, de 11 de julio, de servicios de la sociedad
			de la información y de comercio electrónico (en adelante, LSSI-CE) y
			de la Ley Orgánica 3/2018, de Protección de Datos Personales y
			garantía de los derechos digitales VLL HOME STAGING garantiza la
			protección y confidencialidad de los datos personales, de cualquier
			tipo que nos proporcionen nuestros clientes, de acuerdo con lo
			dispuesto en el Reglamento General de Protección de Datos de Carácter
			Personal.</p>

		</br>
		<p>La Política de Protección de Datos de VLL HOME STAGING descansa en
			el principio de responsabilidad proactiva, según el cual el
			Responsable del tratamiento es responsable del cumplimiento del marco
			normativo y jurisprudencial, siendo capaz de demostrarlo ante las
			autoridades de control correspondientes.</p>

		</br>
		<p>Los datos facilitados serán tratados en los términos establecidos
			en el RGPD, en este sentido VLL HOME STAGING ha adoptado los niveles
			de protección que legalmente se exigen, y ha instalado todas las
			medidas técnicas a su alcance para evitar la pérdida, mal uso,
			alteración, acceso no autorizado por terceros, expuestos a
			continuación. No obstante, el usuario debe ser consciente de que las
			medidas de seguridad en Internet no son inexpugnables.</p>


