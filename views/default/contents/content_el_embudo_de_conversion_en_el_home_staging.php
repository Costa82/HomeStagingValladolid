		<p>Hay muchos tipos de embudos de conversión, dependiendo de si lo que
			vendes es un producto o un servicio, si lo haces físicamente o por
			internet, etc. Se puede conceptualizar todo lo que se quiera, pero
			todos ellos, tienen que representar las diferentes etapas del proceso
			de venta desde el principio hasta el final: la venta.</p>
		</br>
		<p>
