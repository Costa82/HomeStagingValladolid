		<p>Se considera que una persona tarda menos de 30 segundos en decidir
			si la vivienda le gusta o no y que la mayor parte de la decisión la
			toma la parte emocional de su cerebro. ¡Usémoslo!</p>
		</br>
		<p>Hagamos que la visita sea una experiencia positiva desde que entra
			por la puerta. Un distribuidor despejado, limpio y bien iluminado con
			pequeños elementos decorativos o alguna planta, automáticamente
			activa la parte emocional del visitante y desea ver más, continuar la
			visita. Por el contrario, si el distribuidor es un lugar oscuro, con
			un montón de muebles de “la abuela”, que apenas dejan pasar, con las
			paredes grises y olor a cerrado, el cliente realizará la visita, pero
			solo por cortesía. Jamás se volverá a saber de él.</p>
		</br>
		<p>Hoy día, quién más quién menos ha vendido algo por una de tantas
			aplicaciones de segunda mano y en la mayoría de los casos se encarga
