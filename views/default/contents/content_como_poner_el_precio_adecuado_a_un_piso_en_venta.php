		<p class="color_corporativo">
			<strong>1. ¿Qué es el precio de mercado? </strong>
		</p>
		</br>
		<p>Un piso demasiado caro hará que no se venda en años y uno demasiado
			barato probablemente se venda en unos días. <strong>En cualquiera de las dos
			opciones estarás perdiendo mucho dinero</strong>. El éxito está en vender a
			precio de mercado. Pero, <strong><ins>¿Cuál es el precio de mercado?</ins></strong></p>
		</br>
		<p>El precio de mercado es <strong>LO QUE SE PAGA POR LOS PISOS</strong>, que no es lo
			mismo que lo que se pide por ellos. El problema está en que lo que se
			paga suele ser un dato no conocido públicamente, lo saben el
			comprador, el vendedor y el notario. Por lo tanto, cuando pones una
			propiedad a la venta, lo que haces es una estimación de lo que vale y
			con ese precio sales. Posteriormente, podrás asumir alguna rebaja, o
			entrar en <strong>una negociación</strong> y acabar vendiendo, <strong>pero si lo hiciste bien
			no debería haber mucha diferencia del precio de salida</strong>.</p>
		</br>
		<p>Si ha habido mucha diferencia de lo que pediste en origen y por lo
			que vendiste finalmente, habrá pasado tiempo y eso se traduce en
			dinero perdido. El tiempo también es dinero.</p>
		</br>
		<p id="ancla_titulo2">La media de venta de un piso en España es de 7 meses. Hay quien
			piensa que es normal, que claro, que tiene que aparecer el vendedor y
			que una casa no se vende de un día para otro y que el posible
			comprador tiene que encontrar la financiación y no sé cuántas
			historias más.</p>
		</br> </br>

		<p class="color_corporativo">
			<strong>2. Desmontando mitos de por qué no se venden las casas
				rápidamente</strong>
		</p>
		</br>
		<p>Vamos a desmontar los <strong>mitos</strong> uno a uno:</p>
		</br>
		<p><ins>¿Que tiene que aparecer el comprador?</ins></p>
		</br>
		<p>La mitad de la gente que vive de alquiler está buscando casa y los
			que ya la tienen, buscan por cambiar o mejorar. Empresas de la
			construcción, reformas, inmobiliarias, Stagers, tasadores, etc…
			forman un tejido empresarial muy fuerte, que no existiría si no
			hubiera compradores. Pisos se venden a diario, después de permanecer
			en el mercado demasiado tiempo, con la consiguiente pérdida económica
			que eso provoca.</p>
		</br>
		<p><ins>¿Que no se vende de un día para otro?</ins></p>
		</br>
		<p>Estoy de acuerdo en que la compra de una casa es la propiedad más
			costosa que se compra la mayoría de la gente en su vida y que no es
			como adquirir una bici. Pero, por el contrario, todos conocemos a
			alguien que le ha pasado o incluso a nosotros mismos, que vio un piso
			que le gustaba, se decidió rápidamente y cuando llamó ya lo habían
			señalizado otras personas. ¿Qué ha pasado? ¿Que a todos de repente
			nos gusta un mismo piso y vamos a por él?, o que las características
			del piso y el precio son consecuentes.</p>
		</br>
		<p><ins>¿Que el comprador tiene que buscar la financiación?<ins></p>
		</br>
		<p>Vale, es verdad que hasta que se produce la firma con el notario
			pasa algo de tiempo, pero no nos vayamos ahí. Antes de eso está la
			oferta, la negociación y la firma de arras. Si hay interés se puede
			hacer en un día, aunque luego haya que esperar algún tiempo para la
			firma. Y otra cosa, la gente cuando va a comprarse una casa ya ha
			hablado con el banco y en muchos casos tienen hipotecas
			preconcedidas. Tampoco es el problema.</p>
		</br>
		<p id="ancla_titulo3" class="centrado">El problema está en que:</p>
		</br>
		<p class="centrado">
			<strong>¡LAS CASAS EN UN PORCENTAJE ELEVADO SALEN MUY POR ENCIMA DE
				PRECIO DE MERCADO!</strong>
		</p>
		</br> </br>

		<p class="color_corporativo">
			<strong>3. Ejemplo práctico de cómo valorar tu casa y poner un precio
				de salida adecuado</strong>
		</p>
		</br>
		<p class="centrado"><ins>¿Cómo hacer una valoración de mi piso?</ins></p>
		</br>
		<p>Como no sabemos lo que se paga por cada piso, nos vamos a fijar en
			la oferta viva.</p>
		</br>
		<p>Aplicamos el método de comparación reconocido por la normativa
			internacional de <strong>valoración de inmuebles</strong> y la orden ECO/805/2003. Se
			seleccionan de entre los inmuebles en oferta, los que pueden ser
			comparables, por su cercanía y características, al inmueble a
			valorar.</p>
		</br>
		<p>Este método analiza la tipología de tu piso y busca pisos
			semejantes con los que compararlos.</p>
		</br>
		<p>La tipología de una vivienda son las características más
			importantes que lo definen. Metros, zona, altura, orientación,
			¿ascensor?, ¿garaje?, y por supuesto el precio, que es lo que
			tratamos de averiguar.</p>
		</br>
		<p>Por encontrarnos en Valladolid, usaremos la plataforma Idealista,
			pues es donde se mueve prácticamente toda la oferta. Podríamos
			utilizar alguna más, pero el dato apenas variaría y el objetivo del
			post es explicar un método. Solo recomendamos hacerlo si la vivienda
			es especialmente singular, pues no será fácil encontrar elementos con
			los que comparar. Una propiedad de gran superficie, de varias plantas
			tipo DUPLEX, un edificio con valor histórico, vivienda considerada de
			lujo, etc…</p>
		</br>
		<p>El precio de una casa se expresa en €/m2 y más correctamente €/m2
			para una determinada zona.</p>
		</br>
		<p>Es como una ecuación, necesitamos dos variables, para despejar la
			incógnita: El precio.</p>
		</br>
		<p class="centrado letra_grande">€/m2 (Zona)</p>
		</br>
		<p>Sabemos la zona en la que se encuentra y los metros cuadrados que
			tiene.</p>
		</br>
		<p>La zona: Idealista nos permite buscar por distritos, mi
			recomendación es dibujar la zona en concreto. Esto debería ser unas
			pocas calles. No es lo mismo el Paseo de Zorrilla a la altura de
			Caballería que en La Rubia. Como tampoco es el mismo centro la calle
			Santiago que la Calle Paraíso.</p>
		</br>
		<p>Los metros: Hablamos de metros construidos. Cuando compras una
			casa, compras metros construidos. El IBI que pagas cada año, la
			comunidad de vecinos, se calculan con metros construidos. En el
			catastro aparecen metros construidos. <strong>Si pagas por construidos, no
			vendas útiles.</strong></p>
		</br>
		<p>Una vez tengas claro estos dos datos, realiza tu primera búsqueda
			en Idealista. Busca pisos para comprar y dibuja la zona en la que
			realizarás la búsqueda. Cuando muestre los resultados aplica el
			filtro de superficie con una tolerancia superior e inferior del 10% ó
			15%.</p>
		</br>
		<p>De todos los resultados que se muestran, <strong>debes quedarte con
			aquellos que sean semejantes a tu vivienda.</strong> Altura, exterior, con
			ascensor, luminoso, estado de conservación, etc…</p>
		</br>
		<p>En esta lista, el 80% de los pisos rondarán una horquilla de
			precios del 30% aprox. El otro 20% son los que consideramos datos
			atípicos. Generalmente por caros, pero también existe la posibilidad
			de que haya alguno con un precio notablemente menor. Puede ocurrir
			que tengan prisa por vender o que el piso esconda algún tipo de tara.
			Para nuestro estudio vamos a despreciar estos datos atípicos, tanto
			los caros como los baratos. Los sacamos de la lista.</p>
		</br>
		<p><strong>¡Bravo! Ya tienes tu lista de pisos semejantes al tuyo. Desde este
			mismo instante, esa es tu competencia directa.</strong> Los compradores que
			buscan por esa zona es posible que vean todas esas casas.</p>
		</br>
		<p>Imaginemos que el listado definitivo lo forman estos 6 inmuebles*.
		</p>
		</br>
		<p><i>*Estos datos son sacados de un Estudio real que hicimos para la
			venta de un piso en la zona de Francisco Suárez. Se trataba de un
			piso de 90 m2, exterior, con ascensor, altura media y en buen estado.</i></p>
		<img src='./views/default/img/Tabla 1.png' title='Tabla 1'
			alt='Tabla 1' class='img_articulo' /> </br> </br>
		<p>Estos testigos de venta son los definitivos, forman el mercado en
			el que se encuentra tu inmueble. Es la <strong>oferta viva</strong>, no es el precio
			de venta, pero sí es <strong>el precio de mercado</strong>. Por lo tanto, el precio de
			mercado en el que se encuentra nuestro piso está entre 133.990€ y
			168.000€, con una media de 145,315.</p>
		</br>
		<p>Ahora bien, <ins>¿Qué precio poner?</ins> Con estos datos la mayoría de la
			gente, me suele decir:</p>
		</br>
		<ul>
			<li><p>Ponerlo como el más caro, el de 168.000.</p></li>
			<li><p>Pregunto… ¿Y eso por qué?</p></li>
			<li><p>Pues porque si son semejantes, no lo voy a poner más barato,
					dicen.</p></li>
		</ul>
		</br>
		<p>Bueno, sí que me sugieren que sería bueno ponerlo como a 167.000
			para diferenciarnos…</p>
		</br>
		<p>Lo que sucede es que el que sean semejantes no los hace iguales y
			<strong>el comprador se quedará con el que esté mejor</strong>. Moviéndote por esa
			horquilla, tendrás posibilidades de venta, pero…, el primero que se
			venderá será el que tenga mejor calidad/precio y si el de 168.000
			está mejor que el tuyo, se venderá ese.</p>
		</br>
		<p>Ahora os voy a explicar lo que sucederá si se vende la casa de
			168.000 en vez de la vuestra. Vuestro piso de repente es el más caro
			de su mercado. Hay otros 4 pisos semejantes a él y encima son mucho
			más baratos. En este punto no digo que estén mejor, que también puede
			ser. Ahora se vende el piso de 150.000€, no es tan caro y está muy
			bien, se puede entrar a vivir. Actualicemos el mercado.</p>
		<img src='./views/default/img/Tabla 2.png' title='Tabla 2'
			alt='Tabla 2' class='img_articulo' /> </br> </br>
		<p id="ancla_titulo4">Tu piso que, pongamos que lo sacaste a 165.000€, es el más caro de
			todos y con una diferencia grande entre los demás. Se le puede
			considerar incluso un dato atípico. En este punto, han podido pasar 6
			meses o 1 año. La gente lo que suele hacer es bajarlo 10.000€ <i>"para
			ver que tal…"</i>, sin darse cuenta que el mercado se ha ido moviendo
			para abajo y que siguen siendo caros. Sus posibilidades de venta,
			apenas aumentan y seguirán entrando a su mercado viviendas mucho más
			apetecibles que la suya.</p>
		</br> </br>

