			y se envía. En cambio, con las casas, siendo la pertenencia de más
			valor que cualquier persona puede vender en su vida, no se actúa de
			la misma forma.</p>
		</br>
		<p>¿A que a nadie se le ocurriría vender un coche sucio? Con las casas
