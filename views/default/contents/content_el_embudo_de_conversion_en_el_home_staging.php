			gente entre otros 30 anuncios que se han publicado ese día (en una
			ciudad de unos 500mil habitantes). Si a esto le sumas los 30 del día
			anterior y los 30 del día siguiente, una persona que busque piso
			activamente cada 3 días se enfrenta a 100 anuncios nuevos. De todos
			ellos visitará el anuncio de 10. Hará 4 o cinco llamadas de los que
			le gusten y acabará visitando 1 ó 2. Lo más probable es que no llegue
			a hacer oferta por ninguno.</p>
		</br>
