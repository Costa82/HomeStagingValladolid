			dedicamos a las reformas, pero sí que hacemos intervenciones hasta el
			umbral de lo que empezaría a ser una reforma. Cambiamos, mejoramos y
			arreglamos todo aquello que no requiera un permiso de obras
			municipal.</p>
		</br>
		<p>Pintamos techos, paredes…, restauramos puertas, frontales de
