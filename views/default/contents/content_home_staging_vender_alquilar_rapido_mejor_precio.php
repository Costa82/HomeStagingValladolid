		<p>
			El <i>Home Staging</i>, es una técnica de marketing importada de los
			EEUU, que se basa en hacer pequeñas intervenciones en el inmueble,
			con la intención de ponerlo a punto, mejorando su aspecto y por tanto
			llegando a un mayor número de posibles clientes, para su posterior
			venta o alquiler. Se basa en un concepto muy simple, un piso mejor
			puesto, se venderá antes y a un mejor precio, que otro semejante,
			pero peor dispuesto. ¡Es lógico!
		</p>
		</br>
		<p>Aunque el mercado inmobiliario es muy grande, se estima que una
			vivienda, viene a estar en un sub-mercado de entre 5 y 10 pisos. Esto
			significa que, en la zona, habrá ese número de pisos con
			características similares, metros, orientación, habitaciones,
			ascensor, precio, etc… y serán su competencia directa. La pregunta
			es, el cliente que busca piso en ese mercado, ¿por cuál de los 10
			pisos que lo forman se decantará? Asumiendo que de características y
			precio son semejantes, la respuesta es obvia. ¡Por el que mejor está!
		</p>
		</br>
		<p>
			Ahí entra el <i>Staging</i>, ponemos la casa “a punto”, para que
			destaque, sea la más agradable, la mejor iluminada, con los espacios
			mejor definidos y funcionales. De ahí nuestro lema, “Ponemos a punto
			tu casa para vender lo antes posible y al mejor precio”. Se trata de
			que el primer piso que se venda sea el nuestro, de ahí el vender
			antes. Y a mejor precio, porque no tendrás que rebajar el piso para
			que sea apetecible. Lo será por sí mismo, y en algunas ocasiones
			incluso se venderá por un precio mayor.
		</p>
		</br>
		<p>Nuestro trabajo se centra en la vivienda de segunda mano, amueblada
			o vacía. Nos dedicamos a reacondicionarla. Pintamos paredes y techos.
			Tiene que resplandecer y ganar luminosidad. Reparamos todo aquello
			que no funciona, que denota decadencia, un interruptor o enchufe
			averiado, la tapa del WC o una ventana que no cierra. El cliente
			usará esos pequeños defectos en la negociación para rebajar el
			precio.</p>

